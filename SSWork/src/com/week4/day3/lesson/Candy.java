package com.week4.day3.lesson;

public class Candy {


    // props
    private double weight;


    // constructors
    public Candy() {
        super();
    }

    public Candy(double weight) {
        super();
        this.weight = weight;
    }


    // getters and setters
    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }





}
